class Inscritos{
    constructor(
        Nome,
        Email,
        Telefone,
        Instrumento,
        Descricao,
        Status,
        Data_Escricao){
        this.Nome = Nome; 
        this.Email = Email; 
        this.Telefone = Telefone;
        this.Instrumento = Instrumento;
        this.Descricao = Descricao;
        this.Status = Status; 
        this.Data_Escricao = Data_Escricao;  

    }
}

module.exports = Inscritos;
